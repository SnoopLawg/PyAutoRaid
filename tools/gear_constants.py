"""
Central artifact/gear constants for all PyAutoRaid tools.

All slot IDs, stat IDs, set IDs, and mapping tables in ONE place.
Import from here — do NOT hardcode these values in other files.

Source of truth:
  - data/static/artifact_rules.json (live game data via mod static-export)
  - regenerated by `python3 tools/refresh_artifact_truth.py`
  - falls back to embedded constants if the JSON is missing/stale

What's tracked here (game-truth, not handmade):
  - SLOT_*: integer enum (1..9 incl. Cloak/Banner)
  - VALID_PRIMARIES: per-slot primary stats allowed by the game
  - VALID_SUBSTATS: per-slot substats allowed by the game
  - SET_NAMES: id → in-game UI name (Speed, Lifesteal, Toxic, Frenzied, etc.)
  - SET_INTERNAL_NAMES: id → Plarium internal enum name (AttackSpeed, LifeDrain, …)

Slot naming note: the game's static data uses "Cloak" for slot 8;
some legacy code refers to it as "Amulet". Both names work via alias.
"""

