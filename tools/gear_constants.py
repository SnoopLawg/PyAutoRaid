# =============================================================================
# Stat IDs (StatKindId — the mod outputs these)
# Combined with IsAbsolute (flat) to identify the full stat type
# =============================================================================
STAT_HP = 1
STAT_ATK = 2
STAT_DEF = 3
STAT_SPD = 4
STAT_RES = 5
STAT_ACC = 6
STAT_CR = 7
STAT_CD = 8

STAT_NAMES = {
    1: "HP", 2: "ATK", 3: "DEF", 4: "SPD",
    5: "RES", 6: "ACC", 7: "CR", 8: "CD",
}

# Full stat type from (stat_id, is_flat) → human-readable name
def stat_type_name(stat_id, is_flat):
    """Convert stat ID + flat flag to readable name like 'ATK%' or 'HP flat'."""
    base = STAT_NAMES.get(stat_id, f"s{stat_id}")
    if stat_id in (STAT_SPD, STAT_RES, STAT_ACC):
        return base  # always additive
    if stat_id in (STAT_CR, STAT_CD):
        return base + "%"  # always percentage (additive to stat)
    return base + ("_flat" if is_flat else "%")


# =============================================================================
# ArtifactStatKindId (game's internal artifact stat enum, 1-12)
# The mod stores StatKindId (1-8) + IsAbsolute. This table maps back.
# =============================================================================
def to_artifact_stat_kind_id(stat_id, is_flat):
    """Convert (StatKindId, IsAbsolute) to ArtifactStatKindId."""
    if stat_id == STAT_HP:
        return 1 if is_flat else 2
    if stat_id == STAT_ATK:
        return 3 if is_flat else 4
    if stat_id == STAT_DEF:
        return 5 if is_flat else 6
    if stat_id == STAT_SPD:
        return 7
    if stat_id == STAT_RES:
        return 8
    if stat_id == STAT_ACC:
        return 9
    if stat_id == STAT_CR:
        return 10
    if stat_id == STAT_CD:
        return 11
    return 0


# =============================================================================
# Set IDs (ArtifactSetKindId)
#
# SET_NAMES — display-friendly names (HP / ATK / Speed / Lifesteal / etc.)
# kept hand-maintained for UI rendering; sourced from the legacy table.
# SET_KIND_IDS — Plarium-internal enum names (Hp / AttackPower / AttackSpeed
# / LifeDrain) sourced from data/static/artifact_sets.json. Use this when
# bridging to static_data.artifact_set(...).
# SET_BONUSES — derived from static_data so the values are always live-game
# correct (e.g. Speed = 2-piece +12%, AccuracyAndSpeed = +40 ACC + 5% SPD).
# =============================================================================
SET_NAMES: dict[int, str] = {0: "None"}             # id → in-game UI name (Speed, Lifesteal, ...)
SET_INTERNAL_NAMES: dict[int, str] = {}              # id → Plarium internal enum (Hp, AttackPower, ...)
SET_PIECES: dict[int, int] = {}                      # id → 2 / 4 (or 0 for special)
SET_KIND_IDS: dict[int, str] = SET_INTERNAL_NAMES    # legacy alias for existing consumers
SET_BONUSES: dict[int, tuple[int, dict[int, float]]] = {}

# Prefer the live game-truth file (data/static/artifact_rules.json) which is
# generated by `tools/refresh_artifact_truth.py`. Falls back to the older
# static_data module, then to the embedded base-set list.
if _rules_loaded:
    for _s in _rules.get("sets", []):
        _id = _s.get("id")
        if _id is None: continue
        SET_NAMES[_id] = _s.get("display_name") or _s.get("internal_name") or "?"
        SET_INTERNAL_NAMES[_id] = _s.get("internal_name") or ""
        SET_PIECES[_id] = _s.get("pieces") or 0
        # Build SET_BONUSES (stat-bonus sets) from the parsed entries.
        sb = _s.get("stat_bonus")
        if sb and sb.get("stat"):
            _stat_id = {"Health": STAT_HP, "Attack": STAT_ATK, "Defence": STAT_DEF,
                        "Speed": STAT_SPD, "Resistance": STAT_RES, "Accuracy": STAT_ACC,
                        "CriticalChance": STAT_CR, "CriticalDamage": STAT_CD}.get(sb["stat"])
            if _stat_id:
                _val = sb.get("value", 0)
                if not sb.get("absolute"):
                    _val = int(round(_val * 100))  # 0.12 → 12
                SET_BONUSES[_id] = (_s.get("pieces") or 2, {_stat_id: _val})

# MULTI-STAT SET FIX (2026-06-23): artifact_rules.json only carries the SINGULAR
# `stat_bonus` (the first StatBonus), but the game model (IL2CPP ArtifactSetInfo)
# has a `List<StatBonus> StatBonuses` — several sets grant TWO stats
# (Perception = +40 ACC AND +5% SPD, Lethal = +15% ATK AND +5% C.RATE,
# CritDmg+Speed, Speed+Resistance, HpAndDefence, ResistAndDef, etc.). The full
# plural list lives in data/static/artifact_sets.json (from /artifact-sets-truth).
# Merge any missing stats in so SET_BONUSES is the complete game-truth — the
# gear optimizer + fallback stat calc were silently dropping the 2nd stat.
try:
    import json as _json
    from pathlib import Path as _Path
    _sets_path = _Path(__file__).resolve().parent.parent / "data" / "static" / "artifact_sets.json"
    if _sets_path.exists():
        _af = _json.loads(_sets_path.read_text(encoding="utf-8"))
        _rows = next((v for k, v in _af.items() if k != "_meta" and isinstance(v, list)), [])
        _name_to_stat = {"Health": STAT_HP, "Attack": STAT_ATK, "Defence": STAT_DEF,
                         "Speed": STAT_SPD, "Resistance": STAT_RES, "Accuracy": STAT_ACC,
                         "CriticalChance": STAT_CR, "CriticalDamage": STAT_CD}
        for _row in _rows:
            _id = _row.get("id")
            _full = _row.get("stat_bonuses") or []
            if _id is None or len(_full) < 1:
                continue
            _pieces = _row.get("pieces") or SET_PIECES.get(_id) or 2
            _stat_map = dict(SET_BONUSES.get(_id, (_pieces, {}))[1])
            for _b in _full:
                _sid = _name_to_stat.get(_b.get("stat"))
                if _sid is None:
                    continue  # e.g. IgnoreDefence — not a base stat, handled as proc
                _v = _b.get("value", 0)
                if not _b.get("absolute"):
                    _v = round(_v * 100, 2)   # 0.05 -> 5  (percent)
                _stat_map[_sid] = _v          # game-truth value (overwrites/augments)
            if _stat_map:
                SET_BONUSES[_id] = (_pieces, _stat_map)
except Exception:
    pass  # keep the singular-stat table if the plural file is unavailable

